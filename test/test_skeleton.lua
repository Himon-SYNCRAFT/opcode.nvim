h.run("setup is idempotent — second call is a no-op", function()
   h.reset_plugin()
   require("opcode").setup(h.valid_config())
   local done = h.capture_notify()
   require("opcode").setup(h.valid_config({ port = 9999 }))
   h.assert_no_notify(vim.log.levels.ERROR)
   done()
end)

h.run("public API functions error before setup", function()
   h.reset_plugin()
   local done = h.capture_notify()
   local mod = require("opcode")
   mod.connect()
   mod.list_sessions()
   mod.send_file()
   mod.send_selection()
   mod.send_line()
   local notified = done()
   local setup_errors = 0
   for _, n in ipairs(notified) do
      if n.level == vim.log.levels.ERROR and type(n.msg) == "string" and n.msg:find("setup", 1, true) then
         setup_errors = setup_errors + 1
      end
   end
   assert(setup_errors == 5, string.format("Expected 5 setup errors, got %d", setup_errors))
end)

h.run("setup rejects Neovim version < 0.11", function()
   h.reset_plugin()
   local orig_version = vim.version
   local mock = { cmp = function() return -1 end }
   setmetatable(mock, { __call = function() return { major = 0, minor = 10, patch = 0 } end })
   vim.version = mock
   local done = h.capture_notify()
   require("opcode").setup(h.valid_config())
   vim.version = orig_version
   h.assert_notify_contains("Neovim", vim.log.levels.ERROR)
   done()
end)

