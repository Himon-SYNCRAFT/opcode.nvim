package.path = root .. "/test/?.lua;" .. root .. "/lua/?.lua;" .. root .. "/lua/?/init.lua;" .. package.path

local h = require("test_helper")

h.run("setup rejects missing port", function()
   h.reset_plugin()
   local done = h.capture_notify()
   require("opcode").setup({ hostname = "127.0.0.1", command = "alacritty -e opencode --port {port}" })
   h.assert_notify_contains("port", vim.log.levels.ERROR)
   done()
end)

